public class tipedata{
	public static void main(String[] args) {

		//tipe data byte
		byte maksimum = 127;
		byte minimum = -127;
		byte defaultbyte = 0;
		System.out.println(defaultbyte);

		//tipedata short
		short maxshort = 32767;
		short minshort = -32768;
		short defaultshort = 0;
		System.out.println(defaultshort);

		//tipedata integer
		int maxint = 2147483647;
		int minint = -2147483648;
		int defaultint = 0;
		System.out.println(defaultint);

		//tipedata long
		long maxlong = 922337203685477587L;
		long minlong = -922337203685477588L;
		long defaultlong = 0L;
		System.out.println(defaultlong);

		//tipedata float
		float maxfloat = 3.40282347999999999999999999999999999999f;
		float minfloat = -3.40282349999999999999999999999999999999f;
		float defaultfloat = 0f;
		System.out.println(maxfloat);
		System.out.println(minfloat);

		//tipedata double
		double maxdouble = 1.797693134862315702d;
		double mindouble = -1.797693134862315602d;
		double defaultdouble = 0.0d;
		System.out.println(maxdouble);

		//tipedata boolean
		boolean menikah = false;
		System.out.println(menikah);

		//tipedata char
		char A = 'A';
		System.out.println(A);
	}
}